#Operadores aritméticos (continuação)
valor1 = 10
valor2 = 2

print("Exponenciação: ", valor1**3)
print(" Divisão padrão:",valor1/valor2)
print("Divisão sem Decimais",valor1//valor2)
print("Módulo(resto) da Divisão:",valor2%3)

#Operadores de comparação (Relacionais)
print("Valor1 é maior do que valor2:", valor1>valor2)
print("Valor1 é maior ou igual a valor2:", valor1>=valor2)
print("Valor1 é menor que é o valor2:",valor1<valor2)
print("Valor1 é menor igual a valor2:",valor1<=valor2)
print("Valor1 é igual a valor2:",valor1==valor2)
